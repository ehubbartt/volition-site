	<!-- Loot Drops -->
	<section id="loot-drops" class="card">
		<div class="section-header"><h2>Loot Drops</h2></div>
		<p>
			Where applicable, the item <strong>must</strong> be in your chat box where it shows the
			<strong>drop's name</strong>, along with its <strong>value</strong>. To support with evidence
			capture, we would recommend adjusting the
			<em>“Minimum item value needed for loot notification”</em> in the in-game settings.
		</p>
		<p>
			If the item does not show in your chat box, then <strong>pre-collection log</strong> and
			<strong>post-collection log</strong> evidence must be supplied.
		</p>
	</section>

