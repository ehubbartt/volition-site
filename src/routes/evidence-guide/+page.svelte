	<!-- Team Selection -->
	<section id="team-selection" class="card">
		<div class="section-header"><h2>Team Selection Process</h2></div>
		<p>
			When filling out your sign-up form it is important to be as accurate as possible, as this will
			be used to help us form teams. This is usually done with a mixture of available play time
			during the event, EHB &amp; gear. We try our hardest to make the competition as balanced as
			practically possible. For clan vs clan events we aim to have as close to the same amount of
			participants as the other clan.
		</p>
	</section>

	<!-- Wise Old Man Plug-in -->
	<section id="wom" class="card">
		<div class="section-header"><h2>Wise Old Man Plug-in</h2></div>

		<p>The <strong>Wise Old Man plug-in</strong> will be required for the event.</p>
		<p>Shortly before the event starts, a <strong>codeword</strong> will be announced.</p>
		<p>
			This needs to be entered into the <strong>“Codeword”</strong> section of the plug-in settings.
			You must ensure that the <strong>“Show timestamp”</strong> box is ticked so that it shows the
			date and time.
		</p>

		<div class="alert alert-danger">
			<span class="alert-icon" aria-hidden="true">⚠</span>
			<div>
				This is a <strong>firm requirement</strong> for all screenshots submitted as evidence to be
				valid. <strong>There will be no exceptions. No codeword, no points will be awarded.</strong>
			</div>
		</div>

		<p class="note">
			If you need to resize the timestamp box in RuneLite so that everything is visible, this can be
			achieved by holding <code>Alt</code> &amp; dragging the box to the desired size.
		</p>

