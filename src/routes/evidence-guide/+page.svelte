	<!-- Anti-stacking -->
	<section id="anti-stacking" class="card">
		<div class="section-header"><h2>Anti-stacking Evidence</h2></div>
		<p>
			If working on a tile or piece of content which could be pre-emptively stacked, you
			<strong>must</strong> provide evidence that no stacking has taken place.
		</p>
		<p>
			For example, for Clue Scrolls, you must provide evidence of potential caskets, scrolls and boxes
			that may be in the bank. This can be achieved with a search of keywords in the bank like
			<code>scroll</code>.
		</p>

		<div class="alert alert-warning">
			<span class="alert-icon" aria-hidden="true">❗</span>
			<div>
				<strong>It is important that you enter the search so that your IGN &amp; chat box are visible.</strong>
			</div>
		</div>

		<figure class="shot">
			<a href="/evidence-guide/anti-stack-clue-search.jpeg" target="_blank" rel="noopener">
				<img src="/evidence-guide/anti-stack-clue-search.jpeg" alt="Bank search for 'scroll' with the player's IGN and chat box visible" loading="lazy" />
			</a>
			<figcaption>A bank keyword search (e.g. “scroll”) with your IGN &amp; chat box in shot.</figcaption>
		</figure>

		<p>
			If you're working on a tile which <strong>requires points to obtain the loot</strong>, you
			should provide evidence of your starting points, along with your collection log for the specific
			event.
		</p>
		<p>
			For example, at Guardians of the Rift, you should show a <strong>“Check”</strong> of the Reward
			Guardian along with your collection log for GotR.
		</p>

		<div class="alert alert-warning">
			<span class="alert-icon" aria-hidden="true">❗</span>
			<div>
				<strong>Again — it is important that your IGN / chat box etc. is in the screenshot.</strong>
			</div>
		</div>

		<figure class="shot">
			<a href="/evidence-guide/collection-log-gotr.jpeg" target="_blank" rel="noopener">
