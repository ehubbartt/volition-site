		<p class="muted">Item-specific rulings that apply across events.</p>

		<table class="ref">
			<tbody>
				<tr>
					<th>Any Non-PvM Ring</th>
					<td>Ring of Elements, Ring of Endurance, Ring of 3rd Age, Ring of Coins, Ring of Nature, Elven Signet.</td>
				</tr>
				<tr>
					<th>Blood Shards</th>
					<td>
						If pickpocketing, it is imperative to have inventory space, as the drop does not show up in
						the chat box. The Discord loot logger / Dink does not capture it if it drops to the floor.
						<strong>Pre-coll log is a firm requirement.</strong>
					</td>
				</tr>
				<tr>
					<th>Clues</th>
					<td>
						No pre-stacking clue caskets. No pre-stacking implings. Un-completed clue scroll
						pre-stacking is allowed. <strong>Pre-screenshots are required for clue tiles.</strong>
					</td>
				</tr>
				<tr>
					<th>Nihil Shards</th>
					<td>Nihil shards do not count as a GWD unique or a Nex unique.</td>
				</tr>
				<tr>
					<th>Pet drops</th>
					<td>Chompy &amp; Skotizo are always excluded.</td>
				</tr>
				<tr>
					<th>Raids Purples</th>
					<td>
						Raids can be done with anyone in or out of the clan/team. <strong>YOU</strong> must be the
						one to receive the purple. Raids cannot be started until the event has started. Any purples
						obtained close to the start time of the event will be scrutinised heavily.
					</td>
				</tr>
				<tr>
					<th>Slayer Uniques</th>
					<td>Dust Battlestaff, Mist Battlestaff, Imbued Heart, Eternal Gem.</td>
				</tr>
				<tr>
					<th>XP tiles</th>
					<td>Banked XP etc. can be used freely &amp; any XP gained will count towards the total XP across your team.</td>
				</tr>
				<tr>
					<th>Yama Uniques</th>
					<td>Armour or horn. Contracts can be used, but contracts <strong>MUST</strong> have been obtained during the event.</td>
				</tr>
			</tbody>
		</table>
